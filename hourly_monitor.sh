#!/bin/bash
# 每小时自动更新数据

echo "============================================================"
echo "  🔄 Elon Musk 推文数据 - 每小时自动更新"
echo "============================================================"
echo "每 60 分钟自动获取最新数据并更新Excel表格"
echo "按 Ctrl+C 停止"
echo "============================================================"
echo ""

INTERVAL=3600  # 60分钟 = 3600秒

while true; do
    echo "[$(date '+%H:%M:%S')] 开始更新..."
    python3 generate_excel.py
    echo ""
    echo "⏰ 下次更新: $(date -v+1H '+%H:%M')"
    echo "============================================================"
    echo ""

    sleep $INTERVAL
done
